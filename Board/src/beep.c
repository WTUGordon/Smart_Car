#include "include.h"



/**********************************************函数定义*****************************************************
* 函数名字: void beep_init(void)
* 输入参数: void
* 输出参数: void
* 功    能: 蜂鸣器初始化
* 小    组: BBO
* 作    者: by Gordon
* 日    期: 2019/3/3
************************************************************************************************************/
void beep_init(void)
{
    gpio_init (beep, GPO, 0);

}


/**********************************************函数定义*****************************************************
* 函数名字: void beep_on(void)
* 输入参数: void
* 输出参数: void
* 功    能: 蜂鸣器开启
* 小    组: BBO
* 作    者: by Gordon
* 日    期: 2019/3/3
************************************************************************************************************/
void beep_on(void)
{
    gpio_set (beep , 1);
}

/**********************************************函数定义*****************************************************
* 函数名字: void beep_off(void)
* 输入参数: void
* 输出参数: void
* 功    能: 蜂鸣器关闭
* 小    组: BBO
* 作    者: by Gordon
* 日    期: 2019/3/3
************************************************************************************************************/
void beep_off(void)
{
    gpio_set (beep , 0);
}

/**********************************************函数定义*****************************************************
* 函数名字: time_delay_ms(uint32 ms)
* 输入参数: ms
* 输出参数: void
* 功    能: 毫秒延迟
* 小    组: BBO
* 作    者: by Gordon
* 日    期: 2019/3/6
************************************************************************************************************/
void time_delay_ms(uint32 ms)
{
    uint16 i,j;
    while(ms--);
    {
        for(i=0;i<2000;i++)
            for(j=0;j<2000;j++);
    }
}